void ESC1(uint16_t pwm, uint8_t angle) {
	uint16_t error = ADC_value[0] - angle;
	uint16_t angle_speed;
	if(abs(error) >= 40) {
		angle_speed = 0.25 * error;
	}
	else{
		angle_speed = 0;
	}
	Motor1(angle_speed);

}
